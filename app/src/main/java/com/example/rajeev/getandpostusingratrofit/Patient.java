public class Patient {

    public String bldgr;
    public String adress;
    public String city;
    public String contactno;
    public String name;
    public String time;
    public String donar;

    public Patient(String bldgr, String adress, String city, String contactno, String name, String time, String donar) {
        this.bldgr = bldgr;
        this.adress = adress;
        this.city = city;
        this.contactno = contactno;
        this.name = name;
        this.time = time;
        this.donar = donar;
    }
}
